                var triggerTwo = document.querySelector('.trigger-two');
                var triggerThree = document.querySelector('.trigger-three');
                
                var triggerOneTop = triggerOne.offsetTop;
                var triggerTwoTop = triggerTwo.offsetTop;
                var triggerThreeTop = triggerThree.offsetTop;

                document.addEventListener('scroll', () => {

                    
                    if(window.scrollY > triggerOneTop) {
                        triggerOne.querySelector('.content').classList.add('hover');
                    } else {
                        triggerOne.querySelector('.content').classList.remove('hover');
                    }
                    
                    if(window.scrollY > triggerTwoTop) {
                        triggerTwo.querySelector('.content').classList.add('hover');
                    } else {
                        triggerTwo.querySelector('.content').classList.remove('hover');
                    }
                    
                    if(window.scrollY > triggerThreeTop) {
                        triggerThree.querySelector('.content').classList.add('hover');
                    } else {
                        triggerThree.querySelector('.content').classList.remove('hover');
                    }

                })
            
            }
            
        }, 1000);
